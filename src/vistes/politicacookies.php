    <h4>Cookies de rendiment</h4>
    <p>Aquest tipus de Cookie recorda les seves preferències per a les eines que es troben en els serveis, de manera que no ha de tornar a configurar el servei cada vegada que vostè visita. A tall d'exemple, en aquesta tipologia s'inclouen: Ajustaments de volum de reproductors de vídeo o so. Les velocitats de transmissió de vídeo compatibles amb el seu navegador. Els objectes guardats en el "carret de la compra" en els serveis de comerç electrònic com ara botigues.</p>

<h4>Cookies de geo-localització</h4>
<p>Aquestes cookies són utilitzades per esbrinar en quin país es troba quan se sol·licita un servei. Aquesta galeta és totalment anònima, i només s'utilitza per ajudar a orientar el contingut a la seva ubicació.</p>

<h4>Cookies de registre</h4>
<p>Les galetes de registre es generen un cop que l'usuari s'ha registrat o posteriorment ha obert la sessió, i s'utilitzen per identificar-lo en els serveis amb els següents objectius:</p>

<p>Mantenir a l'usuari identificat de manera que, si tanca un servei, el navegador o l'ordinador i en un altre moment o un altre dia torna a entrar en aquest servei, seguirà identificat, facilitant així la seva navegació sense haver de tornar a identificar-se. Aquesta funcionalitat es pot suprimir si l'usuari prem la funcionalitat [tancar sessió], de manera que aquesta galeta s'elimina i la propera vegada que entri al servei l'usuari haurà d'iniciar sessió per estar identificat.</p>

<p>Comprovar si l'usuari està autoritzat per accedir a certs serveis, per exemple, per participar en un concurs.</p>

<p>Addicionalment, alguns serveis poden utilitzar connectors amb xarxes socials com ara Facebook o Twitter. Quan l'usuari es registra en un servei amb credencials d'una xarxa social, autoritza a la xarxa social a guardar una galeta persistent que recorda la seva identitat i li garanteix accés als serveis fins que expira. L'usuari pot esborrar aquesta Cookie i revocar l'accés als serveis mitjançant xarxes socials actualitzant les seves preferències a la xarxa social que específica.</p>

<h4>Cookies de analítiques</h4>
<p>Cada vegada que un usuari visita un servei, una eina d'un proveïdor extern genera una galeta analítica a l'ordinador de l'usuari. Aquesta galeta que només es genera en la visita, servirà en properes visites als serveis d'Victor per identificar de forma anònima a l'visitant. Els objectius principals que es persegueixen són:</p>

<p>Permetre la identificació anònima dels usuaris navegants a través de la galeta (identifica navegadors i dispositius, no persones) i per tant la comptabilització aproximada de el nombre de visitants i la seva tendència en el temps.
Identificar de forma anònima els continguts més visitats i per tant més atractius per als usuaris Saber si l'usuari que està accedint és nou o repeteix visita.</p>

<p>Important: Llevat que l'usuari decideixi registrar-se en un servei de Victor, la galeta mai anirà associada a cap dada de caràcter personal que pugui identificar-lo. Aquestes cookies només seran utilitzades amb propòsits estadístics que ajudin a l'optimització de l'experiència dels usuaris en el lloc.</p>

<h4>Cookies de publicitat</h4>
<p>Aquest tipus de galetes permeten ampliar la informació dels anuncis mostrats a cada usuari anònim en els serveis de Victor. Entre d'altres, s'emmagatzema la durada o freqüència de visualització de posicions publicitàries, la interacció amb les mateixes, o els patrons de navegació i / o comportaments de l'usuari ja que ajuden a conformar un perfil d'interès publicitari. D'aquesta manera, permeten oferir publicitat afí als interessos de l'usuari.</p>

