<h4>¿Què són les cookies?</h4>
<p>Les cookies són un conjunt de dades que un servidor diposita en el navegador d'l'usuari per recollir la informació de registre estàndard d'Internet i la informació de l'comportament dels visitants en un lloc web. És a dir, es tracta de petits arxius de text que queden emmagatzemats al disc dur de l'ordinador i que serveixen per identificar l'usuari quan es connecta novament a el lloc web. El seu objectiu és registrar la visita de l'usuari i guardar certa informació. El seu ús és comú i freqüent a la web ja que permet a les pàgines funcionar de manera més eficient i aconseguir una major personalització i anàlisi sobre el comportament de l'usuari.</p>

<h4>Quins tipus de cookies hi ha?</h4>
<p>Les cookies utilitzades al nostre lloc web, són de sessió i de tercers, i ens permeten emmagatzemar i accedir a informació relativa a l'idioma, el tipus de navegador utilitzat, i altres característiques generals predefinides per l'usuari, així com, seguir i analitzar l'activitat que porta a terme, amb l'objecte d'introduir millores i prestar els nostres serveis d'una manera més eficient i personalitzada.</p>

<p>Les cookies, en funció de la seva permanència, poden dividir-se en galetes de sessió o permanents. Les que expiren quan l'usuari tanca el navegador. Les que expiren en funció de quan es compleixi l'objectiu per al qual serveixen (per exemple, perquè l'usuari es mantingui identificat en els serveis de Victor) o bé quan s'esborren manualment.</p>

